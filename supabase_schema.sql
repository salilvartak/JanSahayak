-- ── Enable UUID extension (already enabled on Supabase) ──────────────────────
CREATE EXTENSION IF NOT EXISTS "pgcrypto";


-- ── 1. devices ───────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS devices (
    id          UUID        PRIMARY KEY,          -- client-generated UUID
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE  devices        IS 'One row per unique device/installation.';
COMMENT ON COLUMN devices.id     IS 'UUID generated by the Flutter app and sent with every request.';


-- ── 2. conversations ─────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS conversations (
    id          UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    device_id   UUID        NOT NULL REFERENCES devices(id) ON DELETE CASCADE,
    title       TEXT        NOT NULL DEFAULT '',
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS conversations_device_id_idx ON conversations (device_id);

COMMENT ON TABLE  conversations IS 'Conversation threads grouped by device.';
COMMENT ON COLUMN conversations.device_id IS 'FK → devices.id';
COMMENT ON COLUMN conversations.title IS 'Optional short label derived from first query.';


-- ── 3. queries ───────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS queries (
    id          UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    device_id   UUID        NOT NULL REFERENCES devices(id) ON DELETE CASCADE,
    conversation_id UUID    REFERENCES conversations(id) ON DELETE SET NULL,
    query_text  TEXT        NOT NULL,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS queries_device_id_idx ON queries (device_id);
CREATE INDEX IF NOT EXISTS queries_conversation_id_idx ON queries (conversation_id);

COMMENT ON TABLE  queries            IS 'Natural-language queries submitted by a device.';
COMMENT ON COLUMN queries.device_id  IS 'FK → devices.id';
COMMENT ON COLUMN queries.conversation_id IS 'FK → conversations.id for grouping multi-turn chats.';
COMMENT ON COLUMN queries.query_text IS 'Raw text of the voice/typed query.';


-- ── 4. images ────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS images (
    id                  UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    device_id           UUID        NOT NULL REFERENCES devices(id) ON DELETE CASCADE,
    query_id            UUID        NOT NULL REFERENCES queries(id) ON DELETE CASCADE,
    original_image_url  TEXT        NOT NULL,
    annotated_image_url TEXT        NOT NULL,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS images_device_id_idx ON images (device_id);
CREATE INDEX IF NOT EXISTS images_query_id_idx  ON images (query_id);

COMMENT ON TABLE  images                      IS 'Original + annotated image pairs produced per query.';
COMMENT ON COLUMN images.original_image_url   IS 'Public Supabase Storage URL of the raw uploaded image.';
COMMENT ON COLUMN images.annotated_image_url  IS 'Public Supabase Storage URL of the YOLO-annotated image.';


-- ============================================================
--  Storage bucket
--  Run this ONLY if not created via the Supabase dashboard.
-- ============================================================

-- Create the bucket (idempotent helper – may error if bucket already exists)
INSERT INTO storage.buckets (id, name, public)
VALUES ('images', 'images', TRUE)
ON CONFLICT (id) DO NOTHING;

-- Allow unauthenticated reads (public bucket)
CREATE POLICY "Public read access"
  ON storage.objects FOR SELECT
  USING ( bucket_id = 'images' );

-- Allow the service role (backend) to insert
CREATE POLICY "Service role insert"
  ON storage.objects FOR INSERT
  WITH CHECK ( bucket_id = 'images' );


