-- ============================================================
--  Migration helper (for existing deployments)
-- ============================================================
-- Run this block once if your project was created before
-- conversation support was added.
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_name = 'queries'
          AND column_name = 'conversation_id'
    ) THEN
        ALTER TABLE queries ADD COLUMN conversation_id UUID;
        ALTER TABLE queries
          ADD CONSTRAINT queries_conversation_id_fkey
          FOREIGN KEY (conversation_id) REFERENCES conversations(id) ON DELETE SET NULL;
    END IF;
END $$;


